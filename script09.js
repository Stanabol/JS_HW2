if (questionTwo == "b" || questionTwo == "B" || questionTwo == 7) {
    score += 2;
    alert(`Это правильный ответ, вам начисляется 2 балла. Ваш результат ${score} балла`);
} else {
    alert(`Не верный ответ. Ваш результат ${score} балла`);
}

