// Задать пользователю 3 вопроса, в каждом вопросе по 3 варианта ответа. За каждый правильный ответ начисляется 2 балла. После вопросов выведите пользователю количество набранных баллов.
let score = 0;
let questionOne = prompt("Какой объект не относится к живой природе?\n Укажите один из вариантов ответа - a, b или c\n a) Бактерии\n b) Водоросли\n c) Песок");

if (questionOne == "c" || questionOne == "C" || questionOne == "Песок" || questionOne == "песок") {
    score += 2;
    alert(`Это правильный ответ, вам начисляется 2 балла. Ваш результат ${score} балла`);
} else {
    alert(`Не верный ответ. Ваш результат ${score} балла`);
}

